// O(N) time, O(N) auxillary space
// Use a min-heap; then find indices (i, j) from the original array

#include <iostream>
#include <algorithm>

using namespace std;

int main(){
    int testcases;
    cin >> testcases;
    for (int x=0; x<testcases; x++){
        int n;
        cin >> n;
        int a[n];
        for (int i=0; i<n; i++){
            cin >> a[i];
        }

        // Use min-heap
    }

    return 0;
}